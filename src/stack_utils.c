#include "push_swap.h"

bool	stack_sorted(t_stack_node *stack)
{
	if (NULL == stack)
		return (1);
	while (stack->next)
	{
		if (stack->value > stack->next->value)
			return (false);
		stack = stack->next;
	}
	return (true);
}

static t_stack_node	*find_highest(t_stack_node *stack)
{
	int				highest;
	t_stack_node	*highest_node;

	if (NULL == stack)
		return (NULL);
	highest = INT_MIN;
	while (stack)
	{
		if (stack->value > highest)
		{
			highest = stack->value;
			highest_node = stack;
		}
		stack = stack->next;
	}
	return (highest_node);
}

// handle input when there are 3 nodes, easy to sort
// if first is the biggest - ra (biggest to bottom)
// if second is the biggest - rra (biggest to bottom)
// biggest at the bottom - check first and second node
void	tiny_sort(t_stack_node **a)
{
	t_stack_node	*highest_node;

	highest_node = find_highest(*a);
	if (*a == highest_node)
		rotate(a, NULL, "ra");
	else if ((*a)->next == highest_node)
		reverse_rotate(a, NULL, "rra");
	if ((*a)->value > (*a)->next->value)
		swap(a, "sa");
}

// handle input when there are 5 nodes
void	handle_five(t_stack_node **a, t_stack_node **b)
{
	while (stack_len(*a) > 3)
	{
		init_nodes(*a, *b);
		finish_rotation(a, find_smallest(*a), 'a');
		push(b, a, "pb");
	}
}